package util.experiment;

/**
 *
 * @author joao
 */
public interface ExperimentManager extends Experiment{

    /**
     * Identifies the experiment unically. It is used by the adapter to output
     * the experiment's results.
     * @return
     */
    public  String getNameID();

    /**
     * String that identifies the result of the experiment and make it comparable
     * with the result of the same getTestId using a different approach.
     * @return
     */
    public String getTestId();


    /**
     * The caption of the variables separeted by white space.
     * @return
     */
    public String getOutputCaptions();
    
    /**
     * The interesting variables separeted by white space.
     * @return
     */
    public String getOutputVariables();
   
}
